package modelo;

public class Apuestas {

    private String id;
    private String valorApuesta;
    private String ganancias;
    private String resultadoApuesta;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getValorApuesta() {
        return valorApuesta;
    }

    public void setValorApuesta(String valorApuesta) {
        this.valorApuesta = valorApuesta;
    }

    public String getGanancias() {
        return ganancias;
    }

    public void setGanancias(String ganancias) {
        this.ganancias = ganancias;
    }

    public String getResultadoApuesta() {
        return resultadoApuesta;
    }

    public void setResultadoApuesta(String resultadoApuesta) {
        this.resultadoApuesta = resultadoApuesta;
    }
}
